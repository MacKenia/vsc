#include <stdio.h>

int main()
{
    int n, arr[100][2], i, j, k, l, m;
    scanf("%d", &n);
    for(int i = 0; i < 2*n; i++)
    {
        scanf("%d %d", &arr[i][0], &arr[i][1]);
    }
    scanf("%d", &i);
    for(i = -500; i <= 500; i++)
    {
        for(j = -500; j <= 500; j++)
        {
            for(k = 0, m = 0, l = 0; k < 2*n; k++)
            {
                if(arr[k][1]>100||arr[k][0]>100||arr[k][1]<(-100)||arr[k][0]<(-100)) continue;
                if (i * arr[k][0] + j * arr[k][1] == 0) break;
                if (i * arr[k][0] + j * arr[k][1] > 0) m++;
                else l++;
            }
            if(m == l && m == n) break;
        }
        if(m == l && m == n) break;
    }
    printf("%d %d\n", i, j);
    return 0;
}


